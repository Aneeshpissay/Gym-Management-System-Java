    private void PayTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PayTableMouseClicked
        try {
            Connection con=MyConnection.getConnection();
            int row=PayTable.getSelectedRow();
            String tab=(PayTable.getModel().getValueAt(row,0).toString());
            String sql="SELECT * FROM gym.payment where pay_id='"+tab+"'";
            
            PreparedStatement ps;
            ps=con.prepareStatement(sql);
            ResultSet rs;
            rs=ps.executeQuery();
            if(rs.next()){
                String id=rs.getString("pay_id");
                ID.setText(id);
                Date pad=rs.getDate("pay_date");
                Pay_date.setDate(pad);
                String pamt=rs.getString("pack_amt");
                pack_amt.setText(pamt);
                String tamt=rs.getString("tot_amt");
                total_amt.setText(tamt);
                Date vu=rs.getDate("valid_upto");
                valid_upto.setDate(vu);
                String amount=rs.getString("prod_price");
                Prod_price.setText(amount);
                String mbrname=rs.getString("mbr_name");
                mbrn.setSelectedItem(mbrname);
                
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_PayTableMouseClicked

