    private void fillmembername(){
        try {
            Connection con=MyConnection.getConnection();
            PreparedStatement ps;
            ps=con.prepareStatement("SELECT * FROM `members`");
            ResultSet rs=ps.executeQuery(); 
            while(rs.next()){
              String mbname=rs.getString("mbr_name");
              mbrn.addItem(mbname);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    public boolean verifyText(){
       if(ID.getText().equals("")||Prod_price.getText().equals("") ||pack_amt.getText().equals("")||Pay_date.getDate()==null||valid_upto.getDate()==null) {
           JOptionPane.showMessageDialog(null, "One or More fields is empty");
           return false;
       }
       else if(Pay_date.getDate().compareTo(new Date() )>0){
           JOptionPane.showMessageDialog(null, "Payment cannot be done in future");
           return false;
       }
       else{
           return true;
       }
        
      
   }
